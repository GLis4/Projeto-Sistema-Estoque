package projetos.atividadeEstoque;

public class Insumo {
	
	int codSistema;
	String nome;
	byte categoria;
	double pCusto;
	byte medida;
	double estoqueM;
	double estoqueA;
	int sitEstoque;

	public void adicionarEstoque(double addEstoque) {
		estoqueA += addEstoque;
	} 
	
	public void removerEstoque(double remEstoque) {
		estoqueA -= remEstoque;
	}

	
}
